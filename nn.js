
function sigmoidDerivative(x) {
  return x * (1 - x);
}
function softmax(x) {
  const maxX = Math.max(...x); // Encuentra el valor m치ximo
  const expX = x.map((v) => Math.exp(v - maxX)); // Resta el m치ximo a cada valor
  const sumExpX = expX.reduce((a, b) => a + b, 0);
  return expX.map((v) => v / sumExpX);
}
function sigmoid(x) {
  return 1 / (1 + Math.exp(-x));
}
function multiplyMatrixVector(matrix, vector) {
  const result = new Array(matrix.length); // Preasignar el arreglo
  for (let i = 0; i < matrix.length; i++) {
    let sum = 0;
    for (let j = 0; j < vector.length; j++) {
      sum += matrix[i][j] * vector[j];
    }
    result[i] = sum; // Asignar directamente en lugar de usar push
  }
  return result;
}
function transposeMatrix(matrix) {
  const rows = matrix.length;
  const cols = matrix[0].length;

  // Preasignar la matriz transpuesta
  const transposed = new Array(cols);
  for (let i = 0; i < cols; i++) {
    transposed[i] = new Array(rows);
  }

  // Rellenar la matriz transpuesta
  for (let i = 0; i < rows; i++) {
    for (let j = 0; j < cols; j++) {
      transposed[j][i] = matrix[i][j];
    }
  }

  return transposed;
}
function addVectors(a, b) {
  let res = new Array(a.length);
  for (let i = 0; i < a.length; i++) {
    res[i] = a[i] + b[i];
  }
  return res;
}
function subtractVectors(a, b) {
  // console.log({a,b})
  let res = new Array(a.length);
  for (let i = 0; i < a.length; i++) {
    res[i] = a[i] - b[i];
  }
  return res;
}
function multiplyVectors(a, b) {
  let res = new Array(a.length); // Preasignar la longitud del arreglo
  for (let i = 0; i < a.length; i++) {
    res[i] = a[i] * b[i];
  }
  return res;
}
